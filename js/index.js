languages.forEach((lang, index) => {
  lang.addEventListener("click", () => {
    languages.forEach((item) => {
      item.classList.remove("active-lang");
    });
    lang.classList.add("active-lang");
    localStorage.setItem("lang", lang.innerHTML);
    currentLang.innerHTML = lang.innerHTML;

    if (lang.innerHTML === "🇷🇺 Ru") {
      changeLang(data.ru);
    } else {
      changeLang(data.eng);
    }
  });
});

if (localStorage.getItem("lang")) {
  currentLang.innerHTML = localStorage.getItem("lang");

  languages.forEach((lang) => {
    if (lang.innerHTML === localStorage.getItem("lang")) {
      lang.classList.add("active-lang");
    } else {
      lang.classList.remove("active-lang");
    }
  });

  if (localStorage.getItem("lang") === "🇷🇺 Ru") {
    changeLang(data.ru);
  } else {
    changeLang(data.eng);
  }
}

function changeLang(lang) {
  // Section-1
  section1Title.innerHTML = lang.section1.title;
  section1SubTitle.innerHTML = lang.section1.subTitle;
  section1Desc1.innerHTML = lang.section1.desc1;
  section1Desc2.innerHTML = lang.section1.desc2;

  sectionBtnText1.forEach((btn) => {
    btn.innerHTML = lang.button.text1;
  });
  sectionBtnText2.forEach((btn) => {
    btn.innerHTML = lang.button.text2;
  });

  // Section-2
  section2Title.innerHTML = lang.section2.title;
  section2Desc1.innerHTML = lang.section2.desc1;
  section2Desc2.innerHTML = lang.section2.desc2;
  section2Bold.innerHTML = lang.section2.bold;

  // Section-3
  section3Title.innerHTML = lang.section3.title;
  section3Desc1.innerHTML = lang.section3.desc1;
  section3Desc2.innerHTML = lang.section3.desc2;
  section3Desc3.innerHTML = lang.section3.desc3;

  section3SubTitle1.innerHTML = lang.section3.subTitle1;
  section3SubTitle2.innerHTML = lang.section3.subTitle2;
  section3SubTitle3.innerHTML = lang.section3.subTitle3;

  section3Services.innerHTML = "";
  lang.section3.services.forEach((item) => {
    const serviceItem = document.createElement("li");
    const serviceBold = document.createElement("b");
    const serviceIcon = document.createElement("i");

    serviceIcon.setAttribute("class", "fa-fw far fa-times-circle");
    serviceIcon.style.color = "rgb(255, 21, 0)";
    serviceBold.innerHTML = item;
    serviceItem.append(serviceIcon, serviceBold);
    section3Services.appendChild(serviceItem);
  });

  // Section-4
  section4Title.innerHTML = lang.section4.title;
  section4Companies.innerHTML= ""

  lang.section4.companies.forEach((item) => {
    const box = document.createElement("div")
    const img = document.createElement("img");

    box.classList.add("company");
    img.src = item.icon;
    box.appendChild(img)

    section4Companies.appendChild(box);
  });

  // Section-5

  section5Title.innerHTML = lang.section5.title
  section5Desc1.innerHTML = lang.section5.desc1
  section5Desc2.innerHTML = lang.section5.desc2
  section5Employers.innerHTML = ""

  lang.section5.empolyeers.forEach(item => {
    const card = document.createElement("div")
    const cardImg = document.createElement("img")
    const cardName = document.createElement("b")
    const cardDesc = document.createElement("p")

    card.classList.add("card")

    cardImg.src = item.img
    cardName.innerHTML = item.name
    cardDesc.innerHTML = item.position
    

    card.append(cardImg, cardName, cardDesc)

    section5Employers.appendChild(card)
  })

  // Section-6
  section6Title.innerHTML = lang.section6.title;
  section6Desc1.innerHTML = lang.section6.desc1;
  section6Desc2.innerHTML = lang.section6.desc2;

  section6SubTitle1.innerHTML = lang.section6.subTitle1;
  section6SubTitle2.innerHTML = lang.section6.subTitle2;

  section6Img.src = lang.section5.empolyeers[0].img
  section6Perfects.forEach(perfects => {
    perfects.innerHTML =""
    lang.section6.perfects.forEach(item => {
      const perfectsItem = document.createElement("li");
      const perfectsText = document.createElement("p");
      const perfectsIcon = document.createElement("i");
  
      perfectsIcon.setAttribute("class", "fa-fw fas fa-check-circle");
      perfectsIcon.style.color = "rgb(252, 55, 146)";
      perfectsText.innerHTML = item;
      perfectsItem.append(perfectsIcon, perfectsText);
      perfects.appendChild(perfectsItem);
    })
  })
}
